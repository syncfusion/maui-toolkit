	/// </summary>
	public enum NumericUpDownPlacementMode
    {
        /// <summary>
        /// The spin buttons are displayed in an expanded, horizontal orientation.
        /// </summary>
        Inline,

        /// <summary> 
        ///   The spin buttons are displayed in an expanded, vertical orientation.
        /// </summary>  
        InlineVertical,
    }
}
