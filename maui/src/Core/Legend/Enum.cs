namespace Syncfusion.Maui.Toolkit
{
    /// <summary>
    /// Represents the default shape types in <see cref="SfShapeView"/>.
    /// </summary>
    public enum ShapeType
    {
        /// <summary>
        /// Specifies a custom shape type. 
        /// </summary>
        Custom,
        /// <summary>
        /// Specifies that the shape is a rectangle
        /// </summary>
        Rectangle,
        /// <summary>
        /// Specifies that the shape is a horizontal line.
        /// </summary>
        HorizontalLine,
        /// <summary>
        /// Specifies that the shape is a circle.
        /// </summary>
        Circle,
        /// <summary>
        /// Specifies that the shape is a diamond.
        /// </summary>
        Diamond,
        /// <summary>
        /// Specifies that the shape is a pentagon.
        /// </summary>
        Pentagon,
        /// <summary>
        /// Specifies that the shape is a triangle.
        /// </summary>
        Triangle,
        /// <summary>
        /// Specifies that the shape is an inverted triangle.
        /// </summary>
        InvertedTriangle,
        /// <summary>
        /// Specifies that the shape is a cross.
        /// </summary>
        Cross,
        /// <summary>
        /// Specifies that the shape is a plus.
        /// </summary>
        Plus,
        /// <summary>
        /// Specifies that the shape is a hexagon.
        /// </summary>
        Hexagon,
        /// <summary>
        /// Specifies that the shape is a vertical line.
        /// </summary>
        VerticalLine
    }

    /// <summary>
    /// Specifies the placement of the legend relative to the plot area, such as top, bottom, left, or right of the chart.
    /// </summary>
    public enum LegendPlacement
    {
        /// <summary>
        /// Positions the legend to the left of the chart.
        /// </summary>
        Left,
        /// <summary>
        /// Positions the legend to the top of the chart.
        /// </summary>
        Top,
        /// <summary>
        /// Positions the legend to the right of the chart.
        /// </summary>
        Right,
        /// <summary>
        /// Positions the legend to the bottom of the chart.
        /// </summary>
        Bottom,
    }
}
