namespace Syncfusion.Maui.Toolkit.Calendar
{
    /// <summary>
    /// Default Calendar font size for all UI elements
    /// </summary>
    internal class CalendarFonts
    {
        #region Internal Methods

        /// <summary>
        /// Default Calendar font size for all header size elements
        /// </summary>
        /// <returns>header font size</returns>
        internal static double GetHeaderTextSize()
        {
            return 14d;
        }

        /// <summary>
        /// Default Calendar font size for all secondary header size elements
        /// </summary>
        /// <returns>Secondary header font size</returns>
        internal static double GetSecondaryHeaderTextSize()
        {
            return 12d;
        }

        /// <summary>
        /// Default Calendar font size for all month body size elements
        /// </summary>
        /// <returns>body font size</returns>
        internal static double GetMonthBodyTextSize()
        {
            return 12d;
        }

        /// <summary>
        /// Default Calendar font size for all year body size elements
        /// </summary>
        /// <returns>body font size</returns>
        internal static double GetBodyTextSize()
        {
            return 14d;
        }

        #endregion
    }
}